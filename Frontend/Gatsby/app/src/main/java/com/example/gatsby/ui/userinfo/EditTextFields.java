package com.example.gatsby.ui.userinfo;

import android.widget.EditText;

import com.example.gatsby.R;

public class EditTextFields {
    EditText Age;
    EditText Name;
    EditText Location;
    EditText Email;
    EditText Rating;

    public EditText getAge() {
        return Age;
    }

    public void setAge(EditText age) {
        Age = age;
    }

    public EditText getName() {
        return Name;
    }

    public void setName(EditText name) {
        Name = name;
    }

    public EditText getLocation() {
        return Location;
    }

    public void setLocation(EditText location) {
        Location = location;
    }

    public EditText getEmail() {
        return Email;
    }

    public void setEmail(EditText email) {
        Email = email;
    }

    public EditText getRating() {
        return Rating;
    }

    public void setRating(EditText rating) {
        Rating = rating;
    }
}
